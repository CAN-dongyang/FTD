// 대상의 분류, 특성
public class Ability
{
	public enum Disposition
	{
		// 긍정 성향
		Passionate,   // 열정적인
		Diligent,     // 성실한
		Optimistic,   // 낙관적인
		Sociable,     // 사교적인
		Orderly,      // 질서정연한

		// 부정 성향
		Apathetic,    // 무관심한
		Slothful,     // 나태한
		Pessimistic,  // 비관적인
		Reclusive,    // 고립적인
		Chaotic       // 혼돈스러운
	}
}

